# Find the sum and average of student's age

# Import Numpy
# pip install numpy
import numpy as np

# Create an array of ages, represented by the local variable, "ages."
ages = [21, 14, 12, 16, 50, 10]

# Calculate the average of ages
average_age = np.mean(ages)

# Calculate the sum of ages
sum_of_ages = np.sum(ages)

# Print out the results in the console
print("Average age:", average_age)
print("Sum of ages:", sum_of_ages)
